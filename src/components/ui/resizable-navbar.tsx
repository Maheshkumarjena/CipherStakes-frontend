export const MobileNavMenu = ({
  children,
  className,
  isOpen,
  onClose,
}: MobileNavMenuProps) => {
  return (
    <AnimatePresence>
      {isOpen && (
        <motion.div
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          exit={{ opacity: 0 }}
          className={cn(
            "absolute inset-x-0 top-16 z-50 flex w-full flex-col items-start justify-start gap-4 rounded-2xl bg-glassmorphism neon-border-blue px-6 py-8 shadow-[0_0_32px_0_hsla(184,100%,50%,0.18)] backdrop-blur-2xl",
            className,
          )}
        >
          {React.Children.map(children, (child) => {
            // Style nav links
            if (React.isValidElement(child) && child.type === 'a') {
              return React.cloneElement(child, {
                className: cn(
                  child.props.className,
                  "text-base font-bold neon-text-blue w-full px-2 py-3 rounded-lg transition-colors duration-200 hover:neon-text-cyan hover:bg-[hsla(184,100%,50%,0.13)] bg-transparent"
                ),
              });
            }
            // Style Connect Wallet button
            if (
              React.isValidElement(child) &&
              child.props &&
              child.props.className &&
              child.props.className.includes("w-full") &&
              child.props.children &&
              typeof child.props.children === "object" &&
              (Array.isArray(child.props.children)
                ? child.props.children.some(
                    (c) => c && c.props && c.props.children && c.props.children === "Connect Wallet"
                  )
                : child.props.children.props && child.props.children.props.children === "Connect Wallet")
            ) {
              return React.cloneElement(child, {
                className: cn(
                  child.props.className,
                  "neon-border-blue neon-text-blue bg-[hsla(184,100%,50%,0.10)] font-bold py-3 px-4 rounded-lg text-base w-full flex items-center justify-center gap-2 hover:bg-[hsla(184,100%,50%,0.18)] transition-all duration-200"
                ),
              });
            }
            return child;
          })}
        </motion.div>
      )}
    </AnimatePresence>
  );
};

export const MobileNavToggle = ({
  isOpen,
  onClick,
}: {
  isOpen: boolean;
  onClick: () => void;
}) => {
  const iconClass =
    "w-8 h-8 p-1 rounded-lg neon-border-blue text-[hsl(var(--neon-blue))] bg-[hsla(184,100%,50%,0.10)] shadow-[0_0_8px_hsl(var(--neon-blue)/0.5)] transition-all duration-200 hover:bg-[hsla(184,100%,50%,0.18)] cursor-pointer";
  return isOpen ? (
    <IconX className={iconClass} onClick={onClick} />
  ) : (
    <IconMenu2 className={iconClass} onClick={onClick} />
  );
};

